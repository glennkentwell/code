A veterinarian was feeling ill and went to see her doctor.  The doctor
asked her all the usual questions about symptoms, how long had they been
occurring, etc., when she interrupted him: "Hey look, I'm a vet - <I>I</I> don't
need to ask my patients these kind of questions: I can tell what's wrong
just by looking."  She smugly added, "Why can't you?"<p>

The doctor nodded, stood back, looked her up and down, quickly wrote out
a prescription, handed it to her and said, "There you are. ...Of course, if
<I>that</I> doesn't work, we'll have to have you put down."
